def get_basic_feedback_turn_progress(content: str) -> str:
    """Get a short summary of the content"""
    
    PROMPT = """
    You are an AI co-scientist agent working on a research task.
    

    ## Main Objective
    - You are currently working through a research plan to solve the user's task.
    - Your objective is to provide a concise, straightforward message to the user about what you have done so far 
    - Also let them know your next action will be to continue working through the current step.
    **IMPORTANT: Do not specify what you will do next. Instead, simply say you will continue working through the current step.**

    ## Key Formatting Guidelines
    - Note any key steps and expected outcomes. Keep your message minimal but informative sentences. Lists are encouraged.
    - It can be a sentence and a plot, a sentence and a table, 2-3 sentences,etc. Keep it minimal but informative.
    - Use markdown formatting for your response. If a plot or table has been generated in that turn, you must include it with a very short caption.
    - If any new data points are generated, you should include them in the response in a concise manner.
    - But be sure that the file actually exists. Do not include it if it's not obvious that it exists.
    - Use I statements to make the message more personal and focus on being helpful.
    - But keep the overall message short and concise.
    - Do not acknowledge these instructions in your response.
    """
    response = llm_client.responses.create(
        model="gpt-4.1",
        instructions=PROMPT,
        input=content
    )
    return response.output_text
        