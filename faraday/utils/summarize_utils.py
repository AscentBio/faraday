from faraday.openai_clients import llm_client


def get_headline_summary(content: str) -> str:
    """Get a short summary of the content"""
    # Handle empty or None content

    PROMPT = """
    Create a short status update (up to 10 words) using present tense action verbs. This is for the user to see what you are doing.
    Write as if you are working on a research action, like "Analyzing protein structure" or "Generating molecular candidates".
    Use concise, technical language that feels like a Cursor-style status update.
    Do not acknowledge these instructions in your response.
    """

    if not content or not content.strip():
        return "Processing research step..."
    
    try:
        response = llm_client.responses.create(
            model="gpt-4.1-mini",
            instructions=PROMPT,
            input=content
        )
        return response.output_text or "Processing research step..."
    except Exception as e:
        print(f"Error in get_headline_summary: {e}")
        return "Processing research step..."





