"""
usage table: supa_usage_table

schema:

create table public.supa_usage_table (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  user_id text null,
  chat_id text null,
  query_id text null,  -- IMPORTANT: Must have UNIQUE constraint for UPSERT to work
  conversation_title text null,
  query_type text null,
  query_subtype text null,
  app_version double precision null,
  status text null,
  total_time double precision null,
  updated_at timestamp without time zone null,
  started_at timestamp without time zone null,
  completed_at timestamp without time zone null,
  duration_seconds integer null,
  num_steps smallint null,
  step_stats json null,
  constraint supa_usage_table_pkey primary key (id),
  constraint supa_usage_table_query_id_key unique (query_id)  -- Required for UPSERT by query_id
) TABLESPACE pg_default;

Usage Flow:
1. Frontend creates stub row with status='pending' and unique query_id
2. Agent UPSERTs by query_id → status='started' then 'running' (updates existing stub)
3. Agent completes → updates by query_id → status='completed' or 'failed'

All updates use query_id as the identifier (not the auto-generated id).
"""

